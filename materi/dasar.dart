void main() {
  // Print String
  print("Hello Word!");
  print("Ayam makan nasi. Minum susu.");
  print("----------------");

  // Print Variabel
  var username = "azlan";
  print(username);
  print("----------------");

  // Print 1 atau lebih variabel dan String
  var nama = "Kudut";
  var kota = "Medan";
  print("Nama saya $nama dan tinggal di $kota.");
  print("----------------");

  // Print Kalkulator Sederhana
  int num1 = 8;
  int num2 = 2;
  int hasilTambah = num1 + num2;
  print("Kalkulator Tambah => $num1 + $num2 = $hasilTambah");
  int hasilKurang = num1 - num2;
  print("Kalkulator Kurang => $num1 - $num2 = $hasilKurang");
  double hasilBagi = num1 / num2;
  print("Kalkulator Kurang => $num1 - $num2 = $hasilBagi");
}
